@bot.command()
async def morse(ctx, text:str):
    morsemess = ''
    for i in text:
        if i in tomorse:
            morsemess += tomorse[i]
        else:
            morsemess += '<?>'
        morsemess += '  '

    await ctx.send(morsemess)
